#include "libft.h"

int	ft_ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		ft_ft_putstr("(null)");
		return (6);
	}
	while (s[i])
	{
		ft_ft_putchar(s[i]);
		i++;
	}
	return (i);
}
