#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lista;

	lista = (t_list *)malloc(sizeof(t_list));
	if (lista == NULL)
		return (NULL);
	lista->content = content;
	lista->next = NULL;
	return (lista);
}
